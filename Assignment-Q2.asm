ORG 0000H

MOV 50H,#09H
MOV 51H,#04H

MOV R0,50H
MOV R1,51H

LOOP:
MOV A,R0
JZ CHECK_A

MOV A,R1
JZ B_ZERO

DEC R0
DEC R1
SJMP LOOP

CHECK_A:
MOV A,R1
JZ EQUAL
MOV 52H,#0FFH     ; A < B
SJMP STOP

B_ZERO:
MOV 52H,#01H      ; A > B
SJMP STOP

EQUAL:
MOV 52H,#00H      ; A = B

STOP: SJMP STOP
END
